	<h3>Graphiques</h3>
	Pendant les élections présidentielles 2022, de nombreuses discussion ont eu lieu autour de la visualisation des sondages. En plus de la question du <a href="https://fr.wikipedia.org/wiki/Wikip%C3%A9dia:Travaux_in%C3%A9dits" target="_blank">travail inédit</a>, la transparence et la vérifiabilité des différentes graphiques pouvait être mise en doute. C’est pourquoi j’ai dévloppé un module pour calculer les moyennes glissantes pondérées, directement dans Wikipédia, visible et corrigeable par tous, ce qui à mis tout le monde d’accord. Le résultat de ce module peut se voir par exemple ici : <a href="https://fr.wikipedia.org/wiki/Liste_de_sondages_sur_l%27%C3%A9lection_pr%C3%A9sidentielle_fran%C3%A7aise_de_2022#Synth%C3%A8se_graphique" target="_blank">Liste de sondages sur l’élection présidentielle française de 2022</a>, et le code est visible ici : <a href="https://fr.wikipedia.org/wiki/Module:Graphique_de_sondages" target="_blank">Module:Graphique de sondages</a>

